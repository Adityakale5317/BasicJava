package Basic_Code;

public class Even_Odd_Whileloop {

	public static void main(String[] args) {
		 {
		        int num = 1;
		        int endRange = 10; 

		        while (num <= endRange) {
		            if (num % 2 == 0) {
		                System.out.println(num + " is even");
		            } else {
		                System.out.println(num + " is odd");
		            }
		            num++;
		        }
		    }

	}

}
