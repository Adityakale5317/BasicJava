package Basic_java;

public class Loops_Java {

	public static void main(String[] args) {
		
		int i = 0;
		while(i <= 10) {
			System.out.print(i+",");
			i++;
		}
		
		for(int j = 1;j<=10;j++) {
			System.out.println(2*j);
	
		}
		
		int k = 0;
		do {
			System.out.println(k);
			k++;
		}while(k<=10);
	}
}
